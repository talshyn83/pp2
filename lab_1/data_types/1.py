#Text Type
x = str("Hello World")	#str

#Numeric Types
x = int(20)	#int
x = float(20.5)	#float	
x = complex(1j)	#complex

#Sequence Types
x = list(("mersedes", "bmw", "porsche"))	#list	
x = tuple(("mersedes", "bmw", "porsche"))	#tuple	
x = range(6)  #range

#Mapping Type
x = dict(name="Gong Yoo", age=45)	#dict

#Set Types
x = set(("mersedes", "bmw", "porsche"))	#set	
x = frozenset(("mersedes", "bmw", "porsche"))	#frozenset	

#Boolean Type
x = bool(5)   #bool	

#Binary Types
x = bytes(5)	#bytes	
x = bytearray(5)	#bytearray